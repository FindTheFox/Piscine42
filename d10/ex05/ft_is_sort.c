int		ft_is_sort(int *tab, int length, int(*f)(int ,int))
{
	int i; 
	
	i = 0;
	while (i < length)
	{
		if (f(tab[i]) == 0)
			i++;
		else if (f(tab[i]) < 0)
			i++;
		else if (f(tab[i]) > 0)
			return(0);
	}
	return (1);
}
