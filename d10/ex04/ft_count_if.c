int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int j;
	int count;

	i = 0;
	count = 0;
	while (tab[i][j])
	{
		j = 0;
		while (tab[i][j] && tab[i][j] == 0)
			j++;
		if (tab[i][j++] == 1)
			count++;
		i++;
	}
	return (0);
}
