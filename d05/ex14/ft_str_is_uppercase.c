#define UP(c) (c >= 'A' && c <= 'Z')

int		ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (UP(str[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
