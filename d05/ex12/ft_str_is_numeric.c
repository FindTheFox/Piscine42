#define NUMBER(c) (c >= '0' && c <= '9' ? 1 : 0)

int	ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (NUMBER(str[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
