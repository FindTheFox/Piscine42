#define LOW(c) (c >= 'a' && c <= 'z' ? 1 : 0)

int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (LOW(str[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
